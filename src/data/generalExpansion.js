const historicalFactionFor = (name, faction) => {
  if (faction === 'wei') return '曹魏';
  if (faction === 'shu') return '蜀汉';
  if (faction === 'wu') return '孙吴';
  if (LIU_SONG_NAMES.has(name)) return '刘宋';
  if (NORTHERN_WEI_NAMES.has(name)) return '北魏';
  if (name === '慕容恪') return '前燕';
  if (name === '慕容德') return '南燕';
  if (name === '慕容农') return '后燕';
  if (name === '王猛') return '前秦';
  if (name === '吕光') return '后凉';
  if (name === '赫连勃勃') return '胡夏';
  if (faction === 'jin') return '东晋';
  if (EASTERN_HAN_NAMES.has(name)) return '东汉';
  if (YUAN_CLAN_NAMES.has(name)) return '河北袁氏';
  if (XIONGNU_NAMES.has(name)) return '南匈奴';
  if (JINGZHOU_NAMES.has(name)) return '荆州刘氏';
  if (LIAODONG_NAMES.has(name)) return '辽东公孙氏';
  if (name === '陈登' || name === '陈珪') return '徐州陈氏';
  return '汉末群雄';
};

